void main (){
  var nilai = 90;
  if (nilai > 70){   //jika nilai besar dari 70, maka ditampilkan nilai A pada layar.
    print("Nilai A");
  } else if (nilai > 40){  //jika nilai besar dari 40, maka ditampilkan nilai B pada layar.
    print("Nilai B");
  } else if (nilai > 0){   //jika nilai besar dari 0, maka ditampilkan nilai C pada layar.
    print("Nilai C");
  } else {
    print("");  //jika tidak, maka ditampilkan text kosong pada layar.
  }
}